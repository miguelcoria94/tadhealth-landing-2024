              <p class="mb-8">
                If you think back to when humans were hunter gatherers during
                prehistoric times, stress was a normal part of each day equated
                to survival for food and shelter. In a mode of survival the
                stress hormone, cortisol, goes up.The spike in cortisol during
                extreme activity or fear narrows arteries, raises the heart
                rate, changes breathing and releases adrenaline (Fairview ADC,
                2020). This kicks our fight or flight response on through the
                endocrine system. In the days of hunters and gatherers, after
                danger had passed or the hunt had finished then cortisol drops
                and the body goes back to a normal state of being. Arteries open
                back up, breathing regulates and the heart rate returns to
                normal. Now the question remains, what happens when we are
                stressed all day aka prolonged stress?
              </p>

              <p class="mb-8">
                It goes without saying that the conditions of how we live now
                versus prehistoric times are very different. Our stressors seem
                to be a lot more constant than the up and down stress of hunting
                and gathering. Ideally our cortisol is raised during resistance
                training and a few times throughout the day. But if you have a
                stressful job or are jam packed with school, that stress tends
                to stay high throughout the day. By the end of a day like that,
                your body may feel wired but tired which is a result of an
                overworked endocrine system (where the adrenaline comes from) .
                The human body was not made to sustain prolonged stress. Our
                endocrine system needs rest to function optimally. As we saw
                above, stress is linked to every single leading cause of death
                in America.
              </p>

              <p class="mb-8">
                So now what? What stress is passable and what isn't? Check in
                with yourself during the day to ask if you feel tense during the
                day. Are your shoulders elevated at your desk? How is your heart
                rate? Do you have intense food cravings at night? All of these
                are ways stress comes into our lives without us realizing it.
                Some stress is normal, so the more we learn about our own
                stressors the more we can understand other ways to reset. Check
                back next week for some tips on how to manage stress during the
                day!
              </p>


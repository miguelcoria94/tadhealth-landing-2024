                  >if there is top leadership behavior modeling and active
                  support.</a
                >
                Let me say that once more for the people in the back.
                <b
                  >The creation of healthy company culture is possible if and
                  only if there is top leadership behavior modeling and active
                  support.</b
                >
                The shame or fear that occurs when leadership doesn't model
                healthy habits, but you want to practice them for yourself.
                Similar to the above example about logging off early, if an
                employee looks up to leadership (as they should) as an example
                of what success looks like, and that leader constantly ignores
                their needs to the point of decimation, the shame cycle of
                prioritizing health further perpetuates. I also recognize that
                not all leaders have a Ph.D. in psychology and that is not the
                ask. With investment and shift in priorities, companies can
                implement health-enhancing supervision and iteratively
                reevaluate worker skills, and job demands, and modify them as
                appropriate.
              </p>


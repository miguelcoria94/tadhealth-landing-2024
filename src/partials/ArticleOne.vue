                63% of Americans are currently participating in the labor force.
                If they’re working full-time, that means they’re spending
                roughly half of their waking hours, each week, supporting their
                company’s financial and operational health. Yet the onus of
                supporting positive mental health is primarily on the employee.
                This is an opportunity to rethink an employer’s role as it
                pertains to mental health prevention efforts.
              </p>

              <p class="mb-8">
                How many times have you heard
                <em>“log off whenever you need”</em> from a manager or employer,
                but then the expectation is for you to complete 80 hours + of
                work before the end of the week? Have you ever been in a mental
                health training that feels game-changing, yet think to yourself,
                <em
                  >“that would be great, but I’m not sure how to apply this and
                  get my work done?”</em
                >
                When our company cultures stress and reward productivity, it’s
                easy to ignore your individual needs in an effort to be valued.
              </p>

              <p class="mb-8">
                Think about the last mental health-related commitment you made
                to yourself. I can go first. I have a goal to get in a 15-minute
                walk every day before work starts, and another one at the end of
                my day. Seems pretty simple, right?
              </p>

              <p class="mb-8">
                It is simple, and yet many of you (myself included) will likely
                follow through with their ritual for only a month or less, or
                until more <em>“pressing”</em> things present a barrier. You
                have to finish that one email. Your boss just slacked you and
                you don’t want to appear like you’re slacking off (no pun
                intended). If you just finish the last 30 minutes of work you’ll
                feel better.
              </p>

              <p class="mb-8">
                That email turns into checking the status of other projects. And
                your manager now needs five things done before tomorrow rather
                than one. And 30 minutes turn into an hour, which then doubles
                once you look outside your window and see that the sun has set,
                and an evening walk is no longer in your future. And this
                hypothetical is all assuming you have a healthy relationship
                with work, a higher than average work/life balance, a somewhat
                decent work culture, and security in your relationships with
                superiors at the company.
              </p>

              <p class="mb-8">
                But what if there’s a power dynamic between an employee and
                their supervisor, whether that may be due to race, class,
                gender, sexual orientation, or age? What if you constantly see
                praises for employees that go
                <em>“above and beyond after work hours?”</em> It would be hard
                not to feel shame in prioritizing your own well-being above your
                workload.
              </p>

              <p class="mb-8">
                These may seem like common everyday occurrences that we all
                shrug off as par for the course. But they’re really microcosms
                of bigger workplace cultural and mental health issues and must
                be examined. These are everyday experiences of talking the talk
                and failing to walk the walk.
              </p>

              <p class="mb-8">
                Like so many pressing issues in our world, the mental health
                crisis will only be properly addressed through true investment
                in evaluating the cultures we’re creating and proliferating:
                which aspects need shedding, which parts still serve us, and
                which other parts need revisiting. It is no longer (and hasn’t
                been) acceptable to continue as if everything’s OK. A singular
                program or a checklist will not offset the deep structural
                issues that create, contribute to, and escalate mental health
                illness. People are dying. We must act. We must imagine new
                possibilities.
              </p>

              <p class="mb-8">
                It’s time to teach people mental health literacy AND provide
                spaces, incentives, and systems that support and celebrate,
                rather than shame and punish mental illness.
              </p>

              <p class="mb-8">But how?</p>
              <p class="mb-8">
                With mental health buzzwords becoming more mainstream, brands
                such as Headspace, Calm, Talk Space and so many others have even
                almost made it <em>“trendy”</em> to partake in meditation as a
                means of self-care. And companies have been quick to get on
                board, offering these apps to employees as part of their
                benefits packages.
              </p>

              <p class="mb-8">
                But to continue moving the needle towards mental wellness,
                workplaces must go beyond free app subscriptions, mandated
                health insurance, and declaring an
                <em>“open door policy.”</em> How a company values its employees’
                mental health and wellbeing needs to be embedded within the
                company culture, so it’s the norm rather than the exception.
                Specifically, the
                <a
                  href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC6331007/"
                  target="_blank"
                  class="text-gray-200 underline hover:no-underline"
                  >design of an individual’s workload</a
                >
                needs to address worker safety, health, and well-being. So we
                cannot say <em>“log off whenever you need to,”</em>
                and also expect folks to complete their 80 hours + worth of
                work.
              </p>

              <p class="mb-8">
                We also need to think about how management structures enable, or
                prevent, employees from truly being seen. Managers, Human
                Resources, and others in leadership need to thoroughly
                understand the physical and psychological job demands, and then
                bring in employees to have an active and valued role in deciding
                how their work will be accomplished
              </p>

              <p class="mb-8">
                In a previous job, the manager that oversaw my team of 15 people
                had no idea of our day-to-day activities. With genuine
                intentions and sensing extreme burnout, she constantly asked
                employees to take mental health days and to provide feedback for
                how her reports could feel more supported. The issue within this
                scenario was that this manager did not have the power to change
                the structures that barred her reports from taking mental health
                days. In other words, it felt more stressful to take a day off
                rather than push through, due to the immense workloads. It was a
                lose-lose situation. Our manager had her hands tied behind her
                back as she slowly watched each of her reports transition out of
                the company. Imagine if the CEO or someone of equal power was
                fully aware of the work those that brought them money were doing
                and could alter workloads to ensure employee satisfaction? Or
                better yet, empower those closest to the work to be involved and
                valued in these decisions?
              </p>

              <p class="mb-8">
                An organization’s leadership modeling positive habits and
                practices flow into an entire company. The creation of healthy
                company culture is possible if and only
                <a
